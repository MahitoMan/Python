# Вероятность того, что львы не поймают антилопу при встрече
p_no_catch = 0.25

# Вероятность того, что антилопы и львы встретятся
p_meet = 0.5

# Вероятность того, что антилопы не будут пойманы в одно из временных окон
p_safe_single_period = (1 - p_meet) + p_meet * p_no_catch

# Общая вероятность того, что антилопы не будут пойманы за день
p_safe_day = p_safe_single_period ** 2

print(f"Вероятность того, что стадо не потеряет никого за день: {p_safe_day:.6f}")